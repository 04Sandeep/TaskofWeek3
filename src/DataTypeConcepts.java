public class DataTypeConcepts
{
 public static void main(String[] args)
 {
  int a=5;
  System.out.println("The value of int is" +" " +a);

  float b = 2.34f;
  System.out.println("The value of float is" +" " +b);

  boolean flag = true;
  System.out.println("The value of boolean is" +" " +flag);

  byte range = 124;
  System.out.println("The value of byte is" +" " +range);

  long c = 1000000L;
  System.out.println("The value of long is" +" " +c);

  double number = 42.3;
  System.out.println("The value of double is" +" " +number);

  char letter = 68;
  System.out.println("The value of char is" +" " +letter);

  String s = "Data Types";
  System.out.println("The value of string is" +" " +s);

  String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
  System.out.println("The value of array is" +" " +cars[0]);
 }
}
 